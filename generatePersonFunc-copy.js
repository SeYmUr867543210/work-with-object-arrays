let generatedPersonsAmount = 10;
export function generateRandomPerson() {
    let personsArr = []
    for (let i = 0; i < generatedPersonsAmount; i++) {
        const age = getRandomNum(100);
        const firstname = firstNames[getRandomNum(firstNames.length)];
        const lastname = lastNames[getRandomNum(lastNames.length)];

        personsArr.push({ firstname, lastname, age })
    }
    return personsArr;
};

function getRandomNum(num) {
    return Math.floor(Math.random() * num);
}
